// Using Brute Force
class CheckNumber{
    public static void main(String arg[]){
        int num = 6;

        if(num > 0){
            System.out.println("The given number is an even number");
        }
        else if(num < 0 ){
            System.out.println("The given number is an odd number");
        }
        else{
            System.out.println("Zero");
        }
        
    }
}