// using if- else statements

class GreatestOfTwo{
    public static void main(String[] a){

        int num1 = 54;
        int num2 = 15;

        if (num1 == num2)
            System.out.println("The given two numbers are equal");

        else if (num1 > num2)
            System.out.println(num1 + " is the greatest number");

        else    
            System.out.println(num2 + "is the greatest number");
    }
}